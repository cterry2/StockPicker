
public class Projection {
	double NextDay;
	double ThirtyDay;
	double SixtyDay;
	
	public double getNextDay(){
		return NextDay;		
	}
	
	public void setNextDay(double price){
		this.NextDay = price;
	}
	
	public double getThirtyDay(){
		return ThirtyDay;
	}
	
	public void setThirtyDay(double price){
		this.ThirtyDay = price;
	}
	
	public double getSixtyDay(){
		return SixtyDay;
	}
	
	public void setSixtyDay(double price){
		this.SixtyDay = price;
	}
}
